/*
    przycisk Start -> W tej metodzie waliduje dane. Jeśli są poprawne ( parametr read = true) wtedy przysyłam je 
    do klasy AntsAlgorithm wywołując jej konstruktor z odpowiednimi parametrami. 
    Następnie startuje wątek oraz drukuje wyniki do textArea.
    */
    private void buttonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonStartActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText(null);
        Params param = new Params(); 
        dataValidation(param);
        if(read == true){
            try {
        AntsAlgorithm algorithm = new AntsAlgorithm(param); 
        algorithm.start(); 
        while(algorithm.isAlive()){
            
            jTextArea1.setText(AntsAlgorithm.getResult());
        }
        }
            catch(IOException | CloneNotSupportedException | NumberFormatException e)
                {
                   jTextArea1.setText("IOException lub CloneNotSupportedException lub NumberFormatException");
                }
        }
        
        
        
        
        
    }//GEN-LAST:event_buttonStartActionPerformed

