 /*
    metoda ktora sprawdza poprawność pobranych danych. Jeśli dane poprawne to przesyła do Klasy Params.
    W przeciwnym wypadku drukuje odpowiedni napis do textArea.
    */
    private void dataValidation(Params param)
	{
		jTextArea1.setText(null);
		try{
			numberAnts = Integer.parseInt(textNumberAnts.getText());
			if(numberAnts < 0){
				jTextArea1.setText("Liczba mrówek nie może być ujemna");
                                read = false;
                        }
			else
				param.setNumberAnts(numberAnts);

                        time = Integer.parseInt(textTime.getText());
                        if(time < 0){
				jTextArea1.setText("Ilość iteracji nie może być ujemna");
                                         read = false;
                        }
			else
				param.setTime(time); 
                        alpha = Double.parseDouble(textAlpha.getText());
                        if(alpha < 0){
				jTextArea1.setText("Parametr alfa nie może być ujemny");
                                         read = false;
                        }
			else
				param.setAlpha(alpha); 
                        beta = Double.parseDouble(textBeta.getText());
                        if(beta < 0){
				jTextArea1.setText("Parametr beta nie może być ujemny");
                                         read = false;
                        }
			else
				param.setBeta(beta); 
                        rho = Double.parseDouble(textRho.getText());
                        if(rho < 0){
				jTextArea1.setText("Współczynnik parowania feromonu nie może być ujemny");
                                         read = false;
                        }
			else
			{
				if(rho > 0 && rho <1)
					param.setRho(rho);
                                else{
					jTextArea1.setText("Współczynnik parowania feromonu musi być z przedziału (0;1)");
                                                 read = false;
                                    }
			}
                        q = Double.parseDouble(textQ.getText());
                        if(q < 0){
				jTextArea1.setText("Ilość pozostawionego feromonu nie może być ujemna");
                                         read = false;
                        }
			else
			{
				
				param.setQ(q);
                                
			}
                        try{
                            if(filename.endsWith(".txt"))
                                param.setFileName(filename);
                            else {
                                jTextArea1.setText("Zły format pliku. Podaj z końcówką .txt");
                                     read = false;
                        }
                        } catch(NullPointerException e)
                        {
                          jTextArea1.setText("Nie podano pliku");
                          read = false;
                        }
                        
        }    
		catch (NumberFormatException e) { 
		read = false; 
		jTextArea1.setText("Niepoprawny format danych"); 
	}
	}
    
